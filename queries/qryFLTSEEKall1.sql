    [ACQUISITIONMODE] Is Null,
    "MissingData",
    IIf(
      [ACQUISITIONMODE] = "Purchase",
      15,
      IIf(
        [ACQUISITIONMODE] = "Lease",
        10,
        IIf([ACQUISITIONMODE] = "Full",-10, 0)
      )
    )
  ) AS ScoreACQUISITION,
  [ScoreCredit] + [ScoreCredit2] + [ScoreFleetSize] + [ScorePercentDC] + [ScoreDCMiles] + [ScoreSLMiles] + [ScoreMaintenance] + [ScoreLifecycle] + [ScoreAcquisition] AS TOTALSCORE,
  IIf(
    [TOTALSCORE] >= 80,
    "Qualifies",
    IIf(
      [TOTALSCORE] >= 70, "Review Required",
      "Does Not Qualify"
    )
  ) AS GRADE,
