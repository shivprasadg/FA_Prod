    [FLTSEEK]![NUMBERDAYCABS] + [FLTSEEK]![NUMBERSLEEPERS]
  ) AS PercentDC,
  IIf(
    [PercentDC] Is Null,
    "Missing Data",
    IIf(
      [PercentDC] >= 0.7,
      15,
      IIf([PercentDC] >= 0.5, 10, 0)
    )
  ) AS ScorePercentDC,
  IIf(
    [FLTSEEK]![MPYDAYCAB] < 80000
    Or [FLTSEEK]![MPYDAYCAB] > 120000,
    -10,
    IIf(
      [FLTSEEK]![MPYDAYCAB] Is Null, "MissingData",
      0
    )
  ) AS ScoreDCMiles,
  IIf(
    [FLTSEEK]![MPYSLEEPER] Is Null,
    "MissingData",
    IIf(
      [FLTSEEK]![MPYSLEEPER] > 130000,-10,
      0
    )
  ) AS ScoreSLMiles,
