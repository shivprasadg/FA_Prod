          [FLTSEEK]![FACREDITRANK] = "4"
          And [FLTSEEK]![CREDITTYPE] = "Privately Held",
          15,
          IIf(
            [FLTSEEK]![FACREDITRANK] = "1",
            10,
            IIf(
              [FLTSEEK]![FACREDITRANK] = "2",
              10,
              IIf(
                [FLTSEEK]![FACREDITRANK] = "3",
                10,
                IIf(
